/// Motor de cálculo das equações PREVENT™ (Predicting Risk of cardiovascular
/// disease EVENTs) da American Heart Association (AHA).
///
/// Referências das equações e coeficientes:
/// - Khan SS et al. "Development and Validation of the American Heart
///   Association's PREVENT Equations". Circulation. 2024;149:430-449
///   (Tabelas Suplementares S5-S12).
/// - Pacote CRAN `preventr` (https://github.com/martingmayer/preventr),
///   fonte dos coeficientes (R/sysdata.rda) e dos vetores de teste usados
///   como gabarito independente em `test/prevent_test.dart`.
///
/// Esta é uma implementação educacional NÃO oficial.
library;

import 'dart:math' as math;

/// Sexo usado na seleção dos coeficientes sexo-específicos.
enum Sex { male, female }

/// Categoria de risco (apenas horizonte de 10 anos).
enum RiskCategory { low, borderline, intermediate, high }

/// Entrada do motor PREVENT.
///
/// Valores fora dos ranges oficiais são "clampados" para os limites:
/// idade 30-79, PAS 90-200 mmHg, CT 130-320 mg/dL, HDL 20-100 mg/dL,
/// eGFR 15-140 mL/min/1.73m², IMC 15-60 kg/m².
class PreventInput {
  const PreventInput({
    required this.age,
    required this.sex,
    required this.sbp,
    required this.totalChol,
    required this.hdl,
    required this.egfr,
    required this.bmi,
    this.diabetes = false,
    this.smoker = false,
    this.onBpMed = false,
    this.onStatin = false,
    this.hba1c,
    this.uacr,
  });

  final int age; // anos (30-79)
  final Sex sex;
  final double sbp; // mmHg (90-200)
  final double totalChol; // mg/dL (130-320)
  final double hdl; // mg/dL (20-100)
  final double egfr; // mL/min/1.73m² (15-140)
  final double bmi; // kg/m² (15-60)
  final bool diabetes;
  final bool smoker;
  final bool onBpMed; // uso de anti-hipertensivo
  final bool onStatin;
  final double? hba1c; // % (opcional; válido 4.5-15)
  final double? uacr; // mg/g (opcional; válido 0.1-25000)
}

/// Estimativa de risco em percentual (0-100) com categoria (só 10 anos).
class RiskEstimate {
  const RiskEstimate(this.percent, this.category);

  final double percent;
  final RiskCategory? category;
}

/// Resultado completo do cálculo PREVENT.
class PreventResult {
  const PreventResult({
    required this.cvd10,
    required this.ascvd10,
    required this.hf10,
    required this.cvd30,
    required this.ascvd30,
    required this.hf30,
    required this.modelUsed,
  });

  final RiskEstimate cvd10;
  final RiskEstimate ascvd10;
  final RiskEstimate hf10;

  /// Null quando idade > 59 (horizonte de 30 anos não se aplica).
  final RiskEstimate? cvd30;
  final RiskEstimate? ascvd30;
  final RiskEstimate? hf30;

  /// 'base' | 'hba1c' | 'uacr' | 'full'
  final String modelUsed;
}

/// Calculadora das equações PREVENT (base e add-ons HbA1c/UACR).
abstract final class PreventCalculator {
  /// Calcula riscos de DCV total, ASCVD e IC em 10 e 30 anos.
  static PreventResult calculate(PreventInput input) {
    final age = input.age.clamp(30, 79);
    final sbp = input.sbp.clamp(90.0, 200.0).toDouble();
    final totalChol = input.totalChol.clamp(130.0, 320.0).toDouble();
    final hdl = input.hdl.clamp(20.0, 100.0).toDouble();
    final egfr = input.egfr.clamp(15.0, 140.0).toDouble();
    final bmi = input.bmi.clamp(15.0, 60.0).toDouble();

    // Variáveis opcionais fora da faixa são descartadas (comportamento
    // `optional_strict = FALSE` do preventr).
    final hba1c = (input.hba1c != null && input.hba1c! >= 4.5 && input.hba1c! <= 15) ? input.hba1c : null;
    final uacr = (input.uacr != null && input.uacr! >= 0.1 && input.uacr! <= 25000) ? input.uacr : null;

    // Seleção do modelo conforme regra oficial: sem opcionais -> base;
    // um opcional -> base + aquele opcional; dois opcionais -> modelo
    // completo (full), tratando SDI como ausente (o app não coleta CEP).
    final String model;
    if (hba1c != null && uacr != null) {
      model = 'full';
    } else if (hba1c != null) {
      model = 'hba1c';
    } else if (uacr != null) {
      model = 'uacr';
    } else {
      model = 'base';
    }

    final terms = _Terms(
      age: age,
      sbp: sbp,
      totalChol: totalChol,
      hdl: hdl,
      egfr: egfr,
      bmi: bmi,
      diabetes: input.diabetes,
      smoker: input.smoker,
      onBpMed: input.onBpMed,
      onStatin: input.onStatin,
      hba1c: hba1c,
      uacr: uacr,
    );

    final sex = input.sex == Sex.female ? 'female' : 'male';

    return PreventResult(
      cvd10: _estimate(model, '10yr', sex, 'cvd', terms, categorize: true),
      ascvd10: _estimate(model, '10yr', sex, 'ascvd', terms, categorize: true),
      hf10: _estimate(model, '10yr', sex, 'hf', terms, categorize: true),
      cvd30: age <= 59 ? _estimate(model, '30yr', sex, 'cvd', terms) : null,
      ascvd30: age <= 59 ? _estimate(model, '30yr', sex, 'ascvd', terms) : null,
      hf30: age <= 59 ? _estimate(model, '30yr', sex, 'hf', terms) : null,
      modelUsed: model,
    );
  }

  /// eGFR pela equação CKD-EPI 2021 (creatinina, livre de raça) — a mesma
  /// usada no desenvolvimento das equações PREVENT.
  ///
  /// eGFR = 142 * min(Scr/κ, 1)^α * max(Scr/κ, 1)^(-1.200) * 0.9938^idade
  ///        * (1.012 se feminino)
  /// κ = 0.7 (F) / 0.9 (M); α = -0.241 (F) / -0.302 (M).
  static double egfrCkdEpi2021({
    required double creatinine,
    required int age,
    required Sex sex,
  }) {
    assert(creatinine > 0, 'creatinine deve ser maior que zero.');
    final female = sex == Sex.female;
    final kappa = female ? 0.7 : 0.9;
    final alpha = female ? -0.241 : -0.302;
    final ratio = creatinine / kappa;
    return 142 *
        math.pow(math.min(ratio, 1.0), alpha) *
        math.pow(math.max(ratio, 1.0), -1.200) *
        math.pow(0.9938, age) *
        (female ? 1.012 : 1.0);
  }

  /// IMC = peso_kg / (altura_m)², com altura informada em centímetros.
  static double bmi({required double weightKg, required double heightCm}) {
    assert(heightCm > 0, 'heightCm deve ser maior que zero.');
    final heightM = heightCm / 100;
    return weightKg / (heightM * heightM);
  }

  static RiskEstimate _estimate(
    String model,
    String time,
    String sex,
    String outcome,
    _Terms t, {
    bool categorize = false,
  }) {
    final coeffs = _coefficients['${model}_$time']!['${sex}_$outcome']!;
    var lp = 0.0;
    coeffs.forEach((term, beta) {
      lp += beta * t.term(term);
    });
    final percent = 100.0 / (1.0 + math.exp(-lp));
    return RiskEstimate(percent, categorize ? _category(percent) : null);
  }

  /// Categorias de risco (somente horizonte de 10 anos):
  /// <5% low; 5-<7.5% borderline; 7.5-<20% intermediate; >=20% high.
  static RiskCategory _category(double percent) {
    if (percent < 5) return RiskCategory.low;
    if (percent < 7.5) return RiskCategory.borderline;
    if (percent < 20) return RiskCategory.intermediate;
    return RiskCategory.high;
  }
}

/// Termos centrados do preditor linear, exatamente como definidos nas
/// equações PREVENT (centragem: idade 55; não-HDL-C 3.5 mmol/L; HDL-C
/// 1.3 mmol/L; PAS 130 mmHg; IMC 25 kg/m²; eGFR 90; HbA1c 5.3%; splines
/// com nós em PAS 110, IMC 30, eGFR 60).
final class _Terms {
  _Terms({
    required int age,
    required double sbp,
    required double totalChol,
    required double hdl,
    required double egfr,
    required double bmi,
    required bool diabetes,
    required bool smoker,
    required bool onBpMed,
    required bool onStatin,
    required double? hba1c,
    required double? uacr,
  }) {
    final a = (age - 55) / 10;
    final nonHdlC = (totalChol - hdl) * 0.02586 - 3.5;
    final hdlC = (hdl * 0.02586 - 1.3) / 0.3;
    final sbpLt110 = (math.min(sbp, 110.0) - 110) / 20;
    final sbpGte110 = (math.max(sbp, 110.0) - 130) / 20;
    final dm = diabetes ? 1.0 : 0.0;
    final smoking = smoker ? 1.0 : 0.0;
    final bmiLt30 = (math.min(bmi, 30.0) - 25) / 5;
    final bmiGte30 = (math.max(bmi, 30.0) - 30) / 5;
    final egfrLt60 = (math.min(egfr, 60.0) - 60) / -15;
    final egfrGte60 = (math.max(egfr, 60.0) - 90) / -15;
    final bpTx = onBpMed ? 1.0 : 0.0;
    final statin = onStatin ? 1.0 : 0.0;

    _values = {
      'age': a,
      'ageSquared': a * a,
      'nonHdlC': nonHdlC,
      'hdlC': hdlC,
      'sbpLt110': sbpLt110,
      'sbpGte110': sbpGte110,
      'dm': dm,
      'smoking': smoking,
      'bmiLt30': bmiLt30,
      'bmiGte30': bmiGte30,
      'egfrLt60': egfrLt60,
      'egfrGte60': egfrGte60,
      'bpTx': bpTx,
      'statin': statin,
      'bpTxSbpGte110': bpTx * sbpGte110,
      'statinNonHdlC': statin * nonHdlC,
      'ageNonHdlC': a * nonHdlC,
      'ageHdlC': a * hdlC,
      'ageSbpGte110': a * sbpGte110,
      'ageDm': a * dm,
      'ageSmoking': a * smoking,
      'ageBmiGte30': a * bmiGte30,
      'ageEgfrLt60': a * egfrLt60,
      // SDI não é coletado pelo app: no modelo completo, SDI é tratado
      // como ausente (termos 4-6 e 7-10 = 0, missing = 1).
      'sdi4To6': 0,
      'sdi7To10': 0,
      'missingSdi': 1,
      'lnUacr': uacr != null ? math.log(uacr) : 0,
      'missingUacr': uacr != null ? 0 : 1,
      'hba1cDm': (hba1c != null && diabetes) ? hba1c - 5.3 : 0,
      'hba1cNoDm': (hba1c != null && !diabetes) ? hba1c - 5.3 : 0,
      'missingHba1c': hba1c != null ? 0 : 1,
      'constant': 1,
    };
  }

  late final Map<String, double> _values;

  double term(String name) => _values[name]!;
}

